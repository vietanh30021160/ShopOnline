    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String user = request.getParameter("user");
        String pass = request.getParameter("pass");
        String fullname = request.getParameter("fullname");
        String phonenum = request.getParameter("phonenum");
        String email = request.getParameter("email");

        // check
        UserDAO d = new UserDAO();

        if (d.doesUserExist(user)) {
            // quay ve dang ki lai
            String ms = "Username existed!!";
            request.setAttribute("error", ms);
            request.getRequestDispatcher("register.jsp").forward(request, response);
        } else if (d.isEmailExsited(email)) {
            // quay ve dang ki lai
            String ms = "Email existed!!";
            request.setAttribute("error", ms);
            request.getRequestDispatcher("register.jsp").forward(request, response);
        } else if (d.isPhoneNumExisting(phonenum)) {
            // quay ve dang ki lai
            String ms = "Phone existed!!";
            request.setAttribute("error", ms);
            request.getRequestDispatcher("register.jsp").forward(request, response);
        }
        // chua co luu vao
        User ac = new User(user, pass, fullname, email, phonenum);
        d.register(ac);
        response.sendRedirect("login.jsp");

    }

